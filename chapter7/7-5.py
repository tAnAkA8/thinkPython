#これを四回繰り返すと既定値はほぼ正解になる
#>>>変化しなくなるまで繰り返す関数を作る

def squareRoot(a, x):
  y = 0
  while True:
    y = (x + a / x) / 2
    print(y)
    if x == y:
      break
    x = y

